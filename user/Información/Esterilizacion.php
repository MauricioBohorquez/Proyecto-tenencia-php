    <div class="parrafo2">
        Nuevos estudios han demostrado que hay una relación entre la esterilización y algunos problemas en 
        la salud de nuestros animales, como determinadas neoplasias y disfunciones articulares; estudios más viejos 
        han demostrado que la esterilización en perros reduce el riesgo de problemas reproductivos, con el cáncer testicular, el cáncer de mama o la piometra. <br/>
        Es recomendable que los animales que no están destinados a la cría deben ser esterilizados, pero para la esterilización el profesional deberá
        tener en cuenta la edad, la raza, el ambiente familiar, el sexo y el comportamiento de la mascota.  
