    <div class="parrafo3">
        <strong>Beneficios:</strong> <br/>
        Las mascotas esterilizadas no tendrán problemas con tumores o quistes relacionados con hormonas sexuales. <br/><br/>
        Las hembras mayormente son las que sufren estos tumores mamarios y también infecciones de útero (PIOMETRA) o en los ovarios. <br/><br/>
        Si la esterilización es realizada antes del primer celo la probabilidad de que padezcan tumores mamarios o infecciones es mínima,
        después del primer celo la probabilidad de padecerlos sube a un 8% y luego del segundo sube hasta 26%, y así sucesivamente, entre más celos tenga la perra más posibilidad hay de padecer estas complicaciones. <br/><br/>
        En las gatas pasa lo mismo, solo que estos tumores mamarios suelen ser más críticos para esta especie. <br/><br/>
        En los machos prevenimos de que se escapen por buscar una perra o gata en celo, para prevenir problemas testiculares, tales como: torciones, tumores, infecciones,
        mayormente relacionados con la próstata, la esterilización en machos es obligatoria en el caso en el caso que uno o los dos testículos no hayan descendido luego del 
        primer año de vida.
    </div>
