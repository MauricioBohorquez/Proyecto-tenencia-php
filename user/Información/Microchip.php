    <h2 class="h21">¿Que es el Microchip?</h2>
    <div class="parrafo2">
        el microchip es un sistema electronico de identificdor de animales, es tan pequeño como un grano de arroz
        y esta conformado por dos partes uno es el sistema electronico la parte con la que podemos rastrarlo y la segunda es la cobertura 
        que es de un vidrio biocompatible suave que no genera alergias.
    </div>
    <h2 class="h22">Implantación del micro chip.</h2>
    <div class="parrafo3">
        Este proceso tiene que ser llevado a cabo por un veterinario profesional, este tiene que introducir la capsula en el cuerpo del animal
        en la nuca ayudado de un inyector para traspasar la piel, este dispositivo contiene unos codigos unicos.<br/> 
        Este procedimiento solo debe realizarce una vez en la vida, la informacion del operro o gato queda registrada en la base de datos 
        del censo canino que tiene la comunidad autonoma, en esta se almacena informacion como: telefono de contacto, dirección, nombre del propietario.<br/>
        Se recomienda implantarlo despues de los dos meses de vida del animal en adelante.
    </div>
